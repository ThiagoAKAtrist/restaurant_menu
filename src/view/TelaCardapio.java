    public void atualizaTabela() {
        List<Cardapio> lista = this.daoCardapio.findAll();
        DefaultTableModel tabela = (DefaultTableModel) tabelaCardapio.getModel();
        tabela.setRowCount(0);
        
        for (Cardapio c: lista) {
            Object[] obj = new Object[] {
                c.getIdcardapio(),
                c.getDiacardapio(),  c.getSaladacardapio(), c.getCarboidratocardapio(),
                c.getGraoscardapio(), c.getProteinacardapio(), c.getBebidacardapio()
            };
            tabela.addRow(obj);
        }
    }
    
    public void preencheDado() {
        int linhaSelecionada = tabelaCardapio.getSelectedRow();
        
        if (linhaSelecionada < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
        else {
            int codigo = Integer.parseInt(tabelaCardapio.getValueAt(linhaSelecionada, 0).toString());
            Cardapio c = this.daoCardapio.find(codigo);
            tfSalada.setText(c.getSaladacardapio());
            tfCarboidrato.setText(c.getCarboidratocardapio());
            tfGraos.setText(c.getGraoscardapio());
            tfProteina.setText(c.getProteinacardapio());
            tfBebida.setText(c.getBebidacardapio());
            
            // Atualize os botões dos dias da semana
            switch (c.getDiacardapio()) {
                case "SEG" -> tgbtSegunda.setSelected(true);
                case "TER" -> tgbtTerca.setSelected(true);
                case "QUA" -> tgbtQuarta.setSelected(true);
                case "QUI" -> tgbtQuinta.setSelected(true);
                case "SEX" -> tgbtSexta.setSelected(true);
            }
        }
    }    

