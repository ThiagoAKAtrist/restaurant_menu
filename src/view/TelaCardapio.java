    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int linhaSelecionada = tabelaCardapio.getSelectedRow();

        if (linhaSelecionada < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar");
            return;
        }
        
        int codigo = Integer.parseInt(tabelaCardapio.getValueAt(linhaSelecionada, 0).toString());
        Cardapio c = this.daoCardapio.find(codigo);
        
        try {
            int i = JOptionPane.showConfirmDialog(null, "Deseja excluir essa cadastro?", "Excluir CLiente", JOptionPane.YES_NO_OPTION);
            if (i == JOptionPane.YES_OPTION) {
                this.daoCardapio.remove(c);
                JOptionPane.showMessageDialog(null, "Cadastro removido");
                atualizaTabela();
            }
            else {
                JOptionPane.showMessageDialog(null, "Operação Cancelada");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro" + e.getMessage());
        }
    }//GEN-LAST:event_btExcluirActionPerformed

